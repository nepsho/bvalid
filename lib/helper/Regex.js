"use strict";
exports.base64 = /^([0-9a-zA-Z+\/]{4})*(([0-9a-zA-Z+\/]{2}==)|([0-9a-zA-Z+\/]{3}=))?$/;
exports.validDomainReg = new RegExp(
  "^" +
    //------Protocol----------
    '(?:(?:https?|ftp)://)?' +

    //------User(Opt)-------------
    "(?:\\S+(?::\\S*)?@)?" +
    "(?:" +
        '(?:' +
        //----------------------------------
            "(?:" +
              "[a-z0-9\\u00a1-\\uffff]" +
              "[a-z0-9\\u00a1-\\uffff_-]{0,62}" +
            ")?" +
            "[a-z0-9\\u00a1-\\uffff]\\."+
        //----------------------------------
        ')+'+
        //Top-level domain format minimum two character
        "(?:[a-z\\u00a1-\\uffff]{2,}\\.?)" +
    ")" +

    //-----Port--------
    "(?::[0-9]{2,5})?" +

    //-----Path--------
    "(?:[/?#]\\S*)?" +

  "$", "i"
);

